const bottomNavItems: NavItem[] = []

function NavLink({ item }: { item: NavItem }) {
  const pathname = usePathname()
  const isActive = pathname === item.href || (item.href !== '/dashboard' && pathname.startsWith(item.href))

  if (item.disabled) {
    return (
      <span className="flex items-center gap-3 rounded-xl px-3 py-2.5 text-sm text-muted-foreground/60 cursor-not-allowed">
        <span className="text-muted-foreground/40">{item.icon}</span>
        <span>{item.label}</span>
        {item.badge && (
          <span className="ml-auto rounded-md bg-muted/50 px-1.5 py-0.5 text-[10px] font-medium text-muted-foreground/60">
            {item.badge}
          </span>
        )}
      </span>
    )
  }

  return (
    <Link
      href={item.href}
      className={`group flex items-center gap-3 rounded-xl px-3 py-2.5 text-sm font-medium transition-all duration-200 ${
        isActive
          ? 'bg-primary/10 text-primary'
          : 'text-muted-foreground hover:bg-muted/60 hover:text-foreground'
      }`}
    >
      <span className={isActive ? 'text-primary' : 'text-muted-foreground group-hover:text-foreground'}>
        {item.icon}
      </span>
      <span>{item.label}</span>
      {item.badge && (
        <span className={`ml-auto rounded-md px-1.5 py-0.5 text-[10px] font-medium ${
          isActive ? 'bg-primary/20 text-primary' : 'bg-muted text-muted-foreground'
        }`}>
          {item.badge}
        </span>
      )}
    </Link>
  )
}

